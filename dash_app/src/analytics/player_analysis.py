def _event_kwargs(row: pd.Series) -> Dict[str, int]:
    """Qualifier kwargs for PV scoring from a raw row."""
    return {
        "through_ball": int(_has_qual(row, *THROUGH_QUAL_COLS)),
        "cross": int(_has_qual(row, *CROSS_QUAL_COLS)),
        "head": int(_has_qual(row, *HEAD_QUAL_COLS)),
        "aerial": int(str(row.get("event_type", "")).strip().lower() == "aerial"),
    }


def compute_player_pva(df: pd.DataFrame, pv: PossessionValueModel) -> pd.DataFrame:
    """
    Per-player Offensive / Defensive / Total PVA for the match.

    Offensive PVA: pva_sequence() over each possession's on-ball events, summed by
    the acting player.  Defensive PVA: score() at the defensive action's location
    (type_id-aware), summed by the acting player.

    Returns a DataFrame: player_name, team_name, off_pva, def_pva, total_pva.
    """
    off_acc: Dict[str, float] = {}
    def_acc: Dict[str, float] = {}
    team_of: Dict[str, str] = {}

    # ── Offensive PVA: per-possession sequence deltas ──
    for poss_id, grp in df.groupby("poss_id"):
        grp = grp.sort_values(["period", "minute", "second", "event_id"])
        events = []
        rows = []
        for _, r in grp.iterrows():
            et = str(r.get("event_type", "")).strip().lower()
            if et not in OFFENSIVE_EVENTS:
                continue
            if pd.isna(r.get("x")) or pd.isna(r.get("y")):
                continue
            kw = _event_kwargs(r)
            events.append({
                "x": float(r["x"]), "y": float(r["y"]),
                "type_id": int(EVENT_TO_TID.get(et, TID_PASS)),
                "outcome": int(r.get("outcome", 1) or 0),
                **kw,
            })
            rows.append(r)
        if not events:
            continue
        pva = pv.pva_sequence(events)
        for r, d in zip(rows, pva):
            name = str(r.get("player_name", "")).strip()
            if not name or name == "nan":
                continue
            off_acc[name] = off_acc.get(name, 0.0) + float(d)
            team_of.setdefault(name, str(r.get("team_name", "")))

    # ── Defensive PVA: location value of out-of-possession actions ──
    for _, r in df.iterrows():
        et = str(r.get("event_type", "")).strip().lower()
        if et not in DEFENSIVE_EVENTS:
            continue
        # Aerials count on both sides; here we only count successful defensive aerials.
        if et == "aerial" and int(r.get("outcome", 0) or 0) != 1:
            continue
        if pd.isna(r.get("x")) or pd.isna(r.get("y")):
            continue
        name = str(r.get("player_name", "")).strip()
        if not name or name == "nan":
            continue
        tid = EVENT_TO_TID.get(et, TID_BALL_RECOV)
        val = pv.score(float(r["x"]), float(r["y"]), type_id=tid,
                       outcome=int(r.get("outcome", 1) or 0))
        def_acc[name] = def_acc.get(name, 0.0) + float(val)
        team_of.setdefault(name, str(r.get("team_name", "")))

    players = set(off_acc) | set(def_acc)
    recs = []
    for name in players:
        off_v = round(off_acc.get(name, 0.0), 4)
        def_v = round(def_acc.get(name, 0.0), 4)
        recs.append({
            "player_name": name,
            "team_name": team_of.get(name, ""),
            "off_pva": off_v,
            "def_pva": def_v,
            "total_pva": round(off_v + def_v, 4),
        })
    res = pd.DataFrame(recs)
    if not res.empty:
        res = res.sort_values("total_pva", ascending=False).reset_index(drop=True)
    return res


def compute_event_pv_map(df: pd.DataFrame, pv: PossessionValueModel) -> pd.DataFrame:
    """
    Per-event PV delta for the full-match event map.

    For on-ball offensive events the delta comes from pva_sequence within the
    possession (value added by that action).  Returns: player_name, team_name,
    minute, event_type, x, y, pv_delta.
    """
    recs: List[dict] = []
    for poss_id, grp in df.groupby("poss_id"):
        grp = grp.sort_values(["period", "minute", "second", "event_id"])
        events, rows = [], []
        for _, r in grp.iterrows():
            et = str(r.get("event_type", "")).strip().lower()
            if et not in OFFENSIVE_EVENTS:
                continue
            if pd.isna(r.get("x")) or pd.isna(r.get("y")):
                continue
            events.append({
                "x": float(r["x"]), "y": float(r["y"]),
                "type_id": int(EVENT_TO_TID.get(et, TID_PASS)),
                "outcome": int(r.get("outcome", 1) or 0),
                **_event_kwargs(r),
            })
            rows.append(r)
        if not events:
            continue
        pva = pv.pva_sequence(events)
        for r, d in zip(rows, pva):
            name = str(r.get("player_name", "")).strip()
            if not name or name == "nan":
                continue
            recs.append({
                "player_name": name,
                "team_name": str(r.get("team_name", "")),
                "minute": int(r.get("minute", 0) or 0),
                "event_type": str(r.get("event_type", "")).title(),
                "x": float(r["x"]), "y": float(r["y"]),
                "pv_delta": round(float(d), 4),
            })
    return pd.DataFrame(recs)


def compute_possession_swings(df: pd.DataFrame, pv: PossessionValueModel) -> pd.DataFrame:
    """
    Rank possessions by net swing.

    JUDGMENT CALL (documented): swing = SUM of |PVA| across the possession's
    on-ball actions.  Rationale: a possession's "story" is the total value churned
    through the chain (a long build-up culminating in a chance), not just the single
    biggest jump — sum-of-absolute rewards multi-action sequences which are the most
    analytically interesting to inspect in the sequence viewer.

    Returns: poss_id, team_name, start_min, n_events, swing (sorted desc).
    """
    recs = []
    for poss_id, grp in df.groupby("poss_id"):
        grp = grp.sort_values(["period", "minute", "second", "event_id"])
        events = []
        for _, r in grp.iterrows():
            et = str(r.get("event_type", "")).strip().lower()
            if et not in OFFENSIVE_EVENTS:
                continue
            if pd.isna(r.get("x")) or pd.isna(r.get("y")):
                continue
            events.append({
                "x": float(r["x"]), "y": float(r["y"]),
                "type_id": int(EVENT_TO_TID.get(et, TID_PASS)),
                "outcome": int(r.get("outcome", 1) or 0),
                **_event_kwargs(r),
            })
        if len(events) < 2:
            continue
        pva = pv.pva_sequence(events)
        swing = float(np.sum(np.abs(pva)))
        first = grp.iloc[0]
        team = str(grp["team_name"].dropna().iloc[0]) if grp["team_name"].notna().any() else ""
        recs.append({
            "poss_id": int(poss_id),
            "team_name": team,
            "start_min": int(first.get("minute", 0) or 0),
            "n_events": len(events),
            "swing": round(swing, 4),
        })
    res = pd.DataFrame(recs)
    if not res.empty:
        res = res.sort_values("swing", ascending=False).reset_index(drop=True)
    return res


def build_possession_sequence(df: pd.DataFrame, poss_id: int,
                              pv: PossessionValueModel) -> Dict[str, Any]:
    """
    Build the node/connector + cumulative-PV data for one possession.

    Uses pva_sequence() (NOT a hand-rolled accumulation loop).  Returns:
      {"nodes": [{player, event_type, x, y, pva, is_team, cum}], "team_name": str}
    where 'is_team' marks the possession-owning team for colour, and 'cum' is the
    running cumulative PV.
    """
    grp = df[df["poss_id"] == poss_id].sort_values(
        ["period", "minute", "second", "event_id"]
    )
    if grp.empty:
        return {"nodes": [], "team_name": ""}

    poss_team = str(grp["team_name"].dropna().iloc[0]) if grp["team_name"].notna().any() else ""
    events, rows = [], []
    for _, r in grp.iterrows():
        et = str(r.get("event_type", "")).strip().lower()
        if et not in OFFENSIVE_EVENTS:
            continue
        if pd.isna(r.get("x")) or pd.isna(r.get("y")):
            continue
        events.append({
            "x": float(r["x"]), "y": float(r["y"]),
            "type_id": int(EVENT_TO_TID.get(et, TID_PASS)),
            "outcome": int(r.get("outcome", 1) or 0),
            **_event_kwargs(r),
        })
        rows.append(r)

    if not events:
        return {"nodes": [], "team_name": poss_team}

    pva = pv.pva_sequence(events)
    cum = float(np.cumsum(pva)[-1]) if pva else 0.0
    running = 0.0
    nodes = []
    for r, d in zip(rows, pva):
        running += float(d)
        nodes.append({
            "player": str(r.get("player_name", "?")),
            "event_type": str(r.get("event_type", "")),
            "x": float(r["x"]), "y": float(r["y"]),
            "pva": round(float(d), 4),
            "minute": int(r.get("minute", 0) or 0),
            "is_team": canonical_name(str(r.get("team_name", ""))) == canonical_name(poss_team),
            "cum": round(running, 4),
        })
    return {"nodes": nodes, "team_name": poss_team, "total": round(cum, 4)}


