def _is_line_break(row: pd.Series) -> bool:
    """NEW: forward pass crossing a vertical-third boundary, ≥ LINE_BREAK_MIN_DX."""
    x0 = row.get("x"); x1 = row.get("pass_end_x")
    if pd.isna(x0) or pd.isna(x1):
        return False
    x0, x1 = float(x0), float(x1)
    if x1 - x0 < LINE_BREAK_MIN_DX:
        return False
    for b in LINE_BREAK_BOUNDARIES:
        if x0 < b <= x1:
            return True
    return False


def _is_progression(row: pd.Series) -> bool:
    """NEW: on-ball action advancing the ball ≥ PROGRESSION_MIN_DX toward goal."""
    x0 = row.get("x"); x1 = row.get("pass_end_x")
    if pd.isna(x0) or pd.isna(x1):
        return False
    return (float(x1) - float(x0)) >= PROGRESSION_MIN_DX


def _is_switch(row: pd.Series) -> bool:
    """DIRECT (qualifier) with geometric fallback."""
    if _has_qual(row, *SWITCH_QUAL_COLS):
        return True
    y0 = row.get("y"); y1 = row.get("pass_end_y")
    if pd.isna(y0) or pd.isna(y1):
        return False
    return abs(float(y1) - float(y0)) >= SWITCH_MIN_WIDTH


def compute_in_possession_kpis(df: pd.DataFrame, regains_by_player: Dict[str, int]) -> pd.DataFrame:
    """Per-player In-Possession KPI table (only DIRECT/REUSE/NEW columns)."""
    recs: Dict[str, Dict[str, Any]] = {}

    def _row(name, team):
        if name not in recs:
            recs[name] = {
                "player_name": name, "team_name": team,
                "passes_attempted": 0, "passes_completed": 0,
                "switches_of_play": 0, "crosses_attempted": 0, "crosses_completed": 0,
                "line_breaks_attempted": 0, "line_breaks_completed": 0,
                "ball_progressions": 0, "take_ons": 0,
                "attempts_at_goal": 0, "goals": 0,
            }
        return recs[name]

    for _, r in df.iterrows():
        name = str(r.get("player_name", "")).strip()
        if not name or name == "nan":
            continue
        team = str(r.get("team_name", ""))
        et = str(r.get("event_type", "")).strip().lower()
        tid = r.get("type_id")
        succ = int(r.get("outcome", 0) or 0) == 1
        rec = _row(name, team)

        if et == "pass":
            rec["passes_attempted"] += 1
            if succ:
                rec["passes_completed"] += 1
            if _is_switch(r):
                rec["switches_of_play"] += 1
            if _has_qual(r, *CROSS_QUAL_COLS):
                rec["crosses_attempted"] += 1
                if succ:
                    rec["crosses_completed"] += 1
            if _is_line_break(r):
                rec["line_breaks_attempted"] += 1
                if succ:
                    rec["line_breaks_completed"] += 1
            if succ and _is_progression(r):
                rec["ball_progressions"] += 1
        elif et == "take on":
            rec["take_ons"] += 1
            if succ and _is_progression(r):
                rec["ball_progressions"] += 1

        if tid in SHOT_TYPE_IDS or et in ("goal", "miss", "saved shot", "post"):
            rec["attempts_at_goal"] += 1
            if et == "goal" or tid == TID_GOAL:
                rec["goals"] += 1

    out = pd.DataFrame(list(recs.values()))
    if out.empty:
        return out
    out["pass_completion_pct"] = np.where(
        out["passes_attempted"] > 0,
        (out["passes_completed"] / out["passes_attempted"] * 100).round(1), 0.0,
    )
    out["line_break_pct"] = np.where(
        out["line_breaks_attempted"] > 0,
        (out["line_breaks_completed"] / out["line_breaks_attempted"] * 100).round(1), 0.0,
    )
    out["possession_regains"] = out["player_name"].map(regains_by_player).fillna(0).astype(int)
    return out


def compute_out_possession_kpis(df: pd.DataFrame, regains_by_player: Dict[str, int]) -> pd.DataFrame:
    """Per-player Out-of-Possession KPI table (only DIRECT/REUSE columns)."""
    recs: Dict[str, Dict[str, Any]] = {}

    def _row(name, team):
        if name not in recs:
            recs[name] = {
                "player_name": name, "team_name": team,
                "tackles_made": 0, "tackles_won": 0, "blocks": 0,
                "interceptions": 0, "clearances": 0,
                "aerial_duels_won": 0, "aerial_duels_total": 0,
            }
        return recs[name]

    for _, r in df.iterrows():
        name = str(r.get("player_name", "")).strip()
        if not name or name == "nan":
            continue
        team = str(r.get("team_name", ""))
        et = str(r.get("event_type", "")).strip().lower()
        succ = int(r.get("outcome", 0) or 0) == 1
        rec = _row(name, team)

        if et == "tackle":
            rec["tackles_made"] += 1
            if succ:
                rec["tackles_won"] += 1
        elif et == "blocked pass":
            rec["blocks"] += 1
        elif et == "interception":
            rec["interceptions"] += 1
        elif et == "clearance":
            rec["clearances"] += 1
        elif et == "aerial":
            rec["aerial_duels_total"] += 1
            if succ:
                rec["aerial_duels_won"] += 1

    out = pd.DataFrame(list(recs.values()))
    if out.empty:
        return out
    out["possession_regains"] = out["player_name"].map(regains_by_player).fillna(0).astype(int)
    return out


def compute_regains_by_player(df: pd.DataFrame, teams: List[str]) -> Dict[str, int]:
    """REUSE detect_high_regains() for both teams; count by acting player."""
    from src.analytics.high_regains import detect_high_regains
    counts: Dict[str, int] = {}
    for team in teams:
        try:
            regains = detect_high_regains(df, team)
        except Exception as exc:
            log.debug("detect_high_regains failed for %s: %s", team, exc)
            continue
        if regains is None or regains.empty:
            continue
        pcol = "player_name" if "player_name" in regains.columns else None
        if not pcol:
            continue
        for name, c in regains[pcol].dropna().value_counts().items():
            counts[str(name)] = counts.get(str(name), 0) + int(c)
    return counts


