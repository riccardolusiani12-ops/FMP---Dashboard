def _load_match_cached(match_csv: Path) -> pd.DataFrame:
    """Load + possession-tag a match once; cache by path + mtime."""
    p = Path(match_csv)
    try:
        mtime = p.stat().st_mtime
    except OSError:
        mtime = 0
    key = f"pa_match_{p.stem}_{mtime}_{_pv_cache_token()}"
    cached = cache_get(key)
    if cached is not None:
        return cached

    df = _load_match_events(p)
    if df is None or df.empty:
        return pd.DataFrame()

    # build_possessions needs event_type/team_id/period/team_name (all present after load).
    df = build_possessions(df)
    cache_set(key, df)
    return df


