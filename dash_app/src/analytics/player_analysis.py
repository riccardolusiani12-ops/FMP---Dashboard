# QUALIFIER HELPER
# ═══════════════════════════════════════════════════════════════════════════════

def _has_qual(row: pd.Series, *cols: str) -> bool:
    """True if any of the named qualifier columns is truthy ('Si'/'Yes'/1/True)."""
    for c in cols:
        v = str(row.get(c, "")).strip().lower()
        if v in ("si", "yes", "1", "true"):
            return True
    return False


