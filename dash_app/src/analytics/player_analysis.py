def analyse_player_analysis(match_csv: Path) -> Dict[str, Any]:
    """
    Compute the full Player Analysis bundle for a match (cached).

    Returns a dict consumed by the component layer:
      teams, full_time, minutes, pva (DataFrame), event_map (DataFrame),
      swings (DataFrame), in_possession (DataFrame), out_possession (DataFrame),
      df (possession-tagged events for the sequence viewer).
    """
    p = Path(match_csv)
    try:
        mtime = p.stat().st_mtime
    except OSError:
        mtime = 0
    bundle_key = f"pa_bundle_{p.stem}_{mtime}_{_pv_cache_token()}"
    cached = cache_get(bundle_key)
    if cached is not None:
        return cached

    df = _load_match_cached(p)
    if df.empty:
        empty = {"teams": [], "df": df, "minutes": {}, "full_time": 90.0,
                 "pva": pd.DataFrame(), "event_map": pd.DataFrame(),
                 "swings": pd.DataFrame(), "in_possession": pd.DataFrame(),
                 "out_possession": pd.DataFrame()}
        return empty

    pv = PossessionValueModel.get_instance()
    teams = [t for t in df["team_name"].dropna().unique().tolist() if str(t).strip()]

    minutes = compute_minutes_played(df)
    pva = compute_player_pva(df, pv)
    event_map = compute_event_pv_map(df, pv)
    swings = compute_possession_swings(df, pv)
    regains = compute_regains_by_player(df, teams)
    in_kpis = compute_in_possession_kpis(df, regains)
    out_kpis = compute_out_possession_kpis(df, regains)

    bundle = {
        "teams": teams,
        "df": df,
        "minutes": minutes,
        "full_time": _match_full_time(df),
        "pva": pva,
        "event_map": event_map,
        "swings": swings,
        "in_possession": in_kpis,
        "out_possession": out_kpis,
    }
    cache_set(bundle_key, bundle)
    return bundle
