# CACHE VERSIONING
# ═══════════════════════════════════════════════════════════════════════════════
# Bump _PIPELINE_VERSION whenever the PV scoring / KPI computation logic in this
# module changes, so cached bundles computed under the old logic are invalidated.
# We ALSO fold in the model file's (mtime, size) so a re-trained / re-packaged
# model invalidates the cache automatically — and, critically, so a cache entry
# computed while the model FAILED to load (sklearn missing → all scores 0.0) is
# never silently served after the model starts loading correctly.  Without this,
# a real fix can appear "not to work" because a stale 0.0 bundle is returned.
_PIPELINE_VERSION = "pv2"


def _pv_cache_token() -> str:
    """A short token that changes when the model file or pipeline version changes."""
    try:
        from src.utils.pv_model import _DEFAULT_MODEL_PATH
        st = Path(_DEFAULT_MODEL_PATH).stat()
        model_sig = f"{int(st.st_mtime)}-{st.st_size}"
    except Exception:
        model_sig = "nomodel"
    # Whether the model actually loaded also belongs in the token: a bundle built
    # with a loaded model must never collide with one built without it.
    try:
        loaded = int(PossessionValueModel.get_instance()._loaded)
    except Exception:
        loaded = 0
    return f"{_PIPELINE_VERSION}-{model_sig}-l{loaded}"


