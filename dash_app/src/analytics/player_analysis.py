def _match_full_time(df: pd.DataFrame) -> float:
    """Data-driven full-time minute (captures stoppage time)."""
    ends = df[(df["type_id"] == TID_END) & (df["period"].fillna(0) >= 2)]
    if not ends.empty:
        return float(ends["minute"].max())
    # Fallback: max minute seen.
    return float(df["minute"].fillna(0).max() or 90.0)


def compute_minutes_played(df: pd.DataFrame) -> Dict[str, Dict[str, Any]]:
    """
    Per-player minutes for a single match.

    Returns {player_name: {"minutes": float, "team": str, "started": bool,
                           "on": float|None, "off": float|None}}.
    """
    full_time = _match_full_time(df)
    out: Dict[str, Dict[str, Any]] = {}

    subs_on  = df[df["type_id"] == TID_PLAYER_ON]
    subs_off = df[df["type_id"] == TID_PLAYER_OFF]
    on_min  = {str(r["player_name"]): float(r["minute"] or 0) for _, r in subs_on.iterrows()
               if pd.notna(r.get("player_name"))}
    off_min = {str(r["player_name"]): float(r["minute"] or 0) for _, r in subs_off.iterrows()
               if pd.notna(r.get("player_name"))}

    # Dismissals (second yellow / red).
    cards = df[df["type_id"] == TID_CARD]
    dismiss_min: Dict[str, float] = {}
    for _, r in cards.iterrows():
        if pd.isna(r.get("player_name")):
            continue
        if _has_qual(r, "Second yellow", "Red Card"):
            dismiss_min[str(r["player_name"])] = float(r["minute"] or full_time)

    # Players who were subbed ON are not starters.
    subbed_on_players = set(on_min.keys())

    # Starting XI per team: first 11 distinct players (by first on-ball event)
    # that were NOT subbed on.
    onball = df[df["player_name"].notna() & df["x"].notna()].sort_values(
        ["period", "minute", "second", "event_id"]
    )
    starters: set = set()
    per_team_count: Dict[str, int] = {}
    for _, r in onball.iterrows():
        name = str(r["player_name"])
        team = str(r.get("team_name", ""))
        if name in subbed_on_players or name in starters:
            continue
        if per_team_count.get(team, 0) >= 11:
            continue
        starters.add(name)
        per_team_count[team] = per_team_count.get(team, 0) + 1

    # Team affiliation per player (last seen team_name on an on-ball event).
    player_team: Dict[str, str] = {}
    for _, r in onball.iterrows():
        if pd.notna(r.get("player_name")):
            player_team[str(r["player_name"])] = str(r.get("team_name", ""))

    all_players = set(player_team.keys()) | subbed_on_players
    for name in all_players:
        start = on_min.get(name, 0.0) if name in subbed_on_players else 0.0
        end_candidates = [full_time]
        if name in off_min:
            end_candidates.append(off_min[name])
        if name in dismiss_min:
            end_candidates.append(dismiss_min[name])
        end = min(end_candidates)
        minutes = max(0.0, min(end - start, full_time))
        out[name] = {
            "minutes": round(minutes, 1),
            "team": player_team.get(name, ""),
            "started": name in starters,
            "on": on_min.get(name),
            "off": off_min.get(name),
            "dismissed": name in dismiss_min,
        }
    return out


